  /**
   * Obtiene el orden de un bot√≥n que se carga en forma vertical como si se cargara en forma horizontal.
   * @return int[] orden
   */
  private int[] getOrdenBotones() {
    int cantElementos = 118;
    int[] orden = new int[cantElementos];
    orden[0] = 0;
    orden[93] = 1;
    orden[1] = 2;
    orden[7] = 3;
    orden[63] = 4;
    orden[69] = 5;
    orden[75] = 6;
    orden[81] = 7;
    orden[87] = 8;
    orden[94] = 9;
    int indice = 2;
    int contador = 10;
    for (int i = 0; i < 5; i ++) {
      orden[indice] = contador;
      contador++;
      indice += 6;
      orden[indice] = contador;
      for (int j = 0; j < 10; j++) {
        indice += 5;
        contador++;
        orden[indice] = contador;
      }
      for (int j = 0; j < 5; j++) {
        indice += 6;
        contador++;
        orden[indice] = contador;
      }
      indice += 7;
      contador++;
      orden[indice] = contador;
      contador++;
      indice -= 92;
    }
    for(; contador < cantElementos; contador++)
      orden[contador] = contador;
    return orden;
  }
  
  /**
   * Ordena los JButtons que se cargan en forma vertical en un array como si se cargaran en forma horizontal.
   * @return Lista de JButtons
   */
  private JButton[] ordenarBotones() {
    Component[] componentes = rootPane.getContentPane().getComponents();
    JButton[] botones = new JButton[118];
    int indice = 0;
    int[] orden = getOrdenBotones();
    for (Component c : componentes) {
      if (c.getClass().toString().equals("class javax.swing.JButton") && !((JButton) c).getText().equals("Salir")) {
        botones[indice++] = (JButton)c;
      }
    }
    return botones;
  }

